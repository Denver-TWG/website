<div class="content">
    <p>The DTWG was founded to bring together folks from across the tech industry in Denver together to try and address some common issues found within our industry.</p>

    <p>Although we're not quite at a place to do everything outlined below, we're hoping to bring people together to build an organization that can follow through on this vision.</p>

    <p>If any of the vision outlined below resonates with you, we'd love for you to <a href="/https://forms.gle/45Qj9TVZDumfL7oi6">sign up for our mailing list</a> and help build the future with us.</p>

    <h2>Reasons to Join</h2>
    <ol>
        <li>
            <h3>Legal & Employment Support</h3>
            <p>As a member of the guild, monthly dues would pay for in-house legal counsel for help with things like going over new employment contracts, navigating severence agreements, or answering labor law & employment questions.</p>
            <p>Dues would also help with paying for career counseling services like coaching, interview prep, and resume review to help members hit the ground running after layoffs or to help you reach the next step of your career.</p>
        </li>
        <li>
            <h3>Independent Compensation Database</h3>
            <p>Have the space to talk about & the tools to track things like: salaries, stock options, percentages, and all other types of compensation in the metro.</p>
            <p>No matter if you're working for the hot new startup in town or a secure and stable state role, we should all get paid fairly for what our work is worth.</p>
        </li>
        <li>
            <h3>Common Space & Community</h3>
            <p>Guild membership would mean access to guildhalls and the people within. Book space for a tlak, social event, or for a quiet workspace away from distractions at home or in the office.</p>
            <p>A common third space for members also means opportunity to share cutting edge ideas, cultivate mentorship opportunities, and network for new career opportunities.</p>
        </li>
        <li>
            <h3>Never Technical Interview Again</h3>
            <p>(Or rather, only do it once.)</p>
            <p>A guild membership plus a guild certificate of technical proficiency would mean that any certified guild member can always be trusted & expected to have the technical chops to do their role, thus rendering technical interviews redundant.</p>
        </li>
        <li>
            <h3>Standardized Career Paths</h3>
            <p>The industry is awash in various titles with ambigious and conflicting connections to skill, tenure, and seniority within a given career.</p>
            <p>The guild would maintain a common and comparable set of titles tied to a rubric of skills to make cross company comparisons trivial.</p>
        </li>
        <li>
            <h3>Common contracts</h3>
            <p>Tired of seeing non-competes, attempts to claim copyright on your off-the-clock work, or any of the other minor indignities put fort in many industry employment contracts?</p>
            <p>The guild aims to establish a common, clean, and fair contract for all guild members to use in their employment.</p>
        </li>
    </ol>
</div>

<div class="smol">
    To protect members, membership is limited to only those without hire-fire power in their workplaces.
